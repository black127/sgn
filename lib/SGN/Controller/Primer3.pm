sub create :Path('/primer3/') :Args(0) {
    my ( $self, $c ) = @_;
	my $form = HTML::FormFu->new(LoadFile($c->path_to(qw{forms primer3 formfu_create.yaml})));

    # Set the template
    $c->stash(template => 'primer3/primer3form.mas',
        form => $form);
}


